let day;
switch (new Date().getDay()) {
  case 0:
    day = "Sunday";
    window.alert(day)
    break;
  case 1:
    day = "Monday";
    window.alert(day)
    break;
  case 2:
    day = "Tuesday";
    window.alert(day)
    break;
  case 3:
    day = "Wednesday";
    window.alert(day)
    break;
  case 4:
    day = "Thursday";
    window.alert(day)
    break;
  case 5:
    day = "Friday";
    window.alert(day)
    break;
  case  6:
    day = "Saturday";
    window.alert(day)
}